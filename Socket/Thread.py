# encoding=utf8 
import time, threading  

# 假定这是你的银行存款:
balance = 0 
lock = threading.Lock()

def change_it(n):
    # 先存后取，结果应该为0:
    global balance
    balance = balance + n
    balance = balance - n

def run_thread_safty(n):
    for i in range(100000):
        #当需要执行change_it（）时1就加入函数
        lock.acquire()
        try:
            # 放心地改吧:
            change_it(n)
        finally:
            # 改完了一定要释放锁:
            lock.release()
        
        
def run_thread(n):#原始run_thread种change_it（）没有枷锁，可能导致紊乱的现象
    for i in range(100):
        change_it(n)

t1 = threading.Thread(target=run_thread, args=(5,))
t2 = threading.Thread(target=run_thread, args=(8,))
t1.start()
t2.start()
t1.join()
t2.join()
print(balance)
